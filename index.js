const prompt = async (i) => {
    const tv = tvs[i]
    const res = await fetch(`https://bible-api.com/${tv}?translation=kjv`)
    const json = await res.json()
    const ref = json.reference
    const line = json.text.replace(/\r\n|\r|\n/g, ' ').trim()

    const header = `====== SCRIPTURE #${i + 1}  ${ref} ======`
    clear()

    log(`
${header}
Let's learn Scripture by typing it out together! 
Type and say outloud the scripture verse exactly as is shown below:

${ref} ${line}
`)

    rl.question(ref + ' ', () => {
        clear()
        log(`
${header}
Awesome job! Ok, now try and type it from memory.
Don't worry if you mess it up you'll get another chance!
Repetition is the essence of perfection!

Once you have it completely correct, you'll advance to the next verse!

`)

        rl.question(ref + ' ', input => {
            const correct = input === line
            clear()
            
            if (!correct) {
                log(`
${header}
S O R R Y !
    :'(            

That was not the correct answer. 
Compare your answer to the correct one to see where you went wrong:

vvv CORRECT ANSWER
${ref} ${line}
${ref} ${input}
^^^ YOUR ANSWER

`)
            } else {
                log(`
${header}
CORRECT!  
Great job on learning ${ref}: 

    "${input}"
    - ${ref} (KJB)

`)
            }

            return rl.question('PRESS ENTER TO CONTINUE...', () => {
                return prompt(i + (correct ? 1 : 0))
            })
        })
    })
}

